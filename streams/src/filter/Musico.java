package filter;

public class Musico {
    final String nome;
    final String intrumentoQueToca;
    final int idade;
    final int tempoExperiencia;

    public Musico(String nome, String intrumento, int idade, int tempoExperiencia) {
        this.nome = nome;
        this.intrumentoQueToca = intrumento;
        this.idade = idade;
        this.tempoExperiencia = tempoExperiencia;
    }
}
